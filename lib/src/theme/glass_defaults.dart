/// Shared numeric constants used across glass_ui_kit widgets for spacing,
/// sizing, and radius values.
class GlassDefaults {
  /// Standard corner radius (12.0) for glass card surfaces.
  static const double borderRadius = 12.0;

  /// Compact inner padding (8.0) — used inside chips and small containers.
  static const double paddingSmall = 8.0;

  /// Default inner padding (16.0) — used by most glass widgets.
  static const double paddingMedium = 16.0;

  /// Spacious inner padding (24.0) — used by dialog-style surfaces.
  static const double paddingLarge = 24.0;

  /// Default diameter (96.0) for the glass loading indicator.
  static const double defaultLoaderSize = 96.0;

  /// Standard height (54.0) for glass buttons.
  static const double buttonHeight = 54.0;

  const GlassDefaults._();
}